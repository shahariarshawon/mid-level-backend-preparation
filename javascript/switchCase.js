// this is called switch case which is used when we have to work base on  a single case handling


let status = null
switch (status) {

    case "pending":
        console.log("Status is pending")
        break;

    case "Submitted":
        console.log("Satus is submitted");
        break;

    case "Failed":
        console.log("Status is failed")
        break

    default:
        console.log("Satus is unknownnnnn")


}

