/**
 * Variable means where we store the data.
 * There are 3 types of variable exists.
 * 1. var [we will never use it]
 * 2. let
 * 3. const
 */
// let value can be changed
let value = "shawon";
value = "rahman";
console.log(value);

// const value can not be changed
const name = "Arafat";
// name = "shawon";
console.log(name);
